#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	char	*ptr;

	if (!s)
		return (0);
	i = 0;
	ptr = (char *)malloc((sizeof(char) * ft_strlen(s)) + 1);
	if (!ptr)
		return (0);
	while (s[i])
	{
		ptr[i] = (*f)(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
